# ce programme a pour but de convertir une température donnée
# en Fahranheit vers le celsius

fahr = 50                          # déclaration de la variable fahr
celsius = (fahr - 32) / 1.8         # conversion en celsius

# affichage du résultat
print(fahr, "° Fahrenheit correspond à", celsius, "° Celsius")