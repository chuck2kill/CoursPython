# ce programme a pour but d'afficher des étoiles en escalier sur 7 lignes

etoile = 1                      # initialisation du nombre d'étoiles

while etoile <= 7:              # boucle jusqu'à 7
    print("*" * etoile)         # affichage du nombre d'étoiles en fonction de la variable 'etoile'
    etoile = etoile + 1         # incrémentation de la variable 'etoile'